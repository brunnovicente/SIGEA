    private void salvar(){
        Real real = new Real();
        real.setCodigoR(Integer.parseInt(jcodigor.getText()));
     
        real.setNome(jnome.getText());
        real.setDataCongelamento(jdata.getText());
        real.setRepetida(jrepetida.getText());
        real.setCaixa(Integer.parseInt(jcaixa.getText()));
        real.setLocal(jlocal.getText());
        
        Banco.getBanco().cadastrar(real);
        JOptionPane.showMessageDialog(null, "Amostra cadastrada com sucesso!");
        jbutaosalvar.setEnabled(false);
    }
    

