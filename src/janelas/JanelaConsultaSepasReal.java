     private void limparTabela(){
        DefaultTableModel model = (DefaultTableModel) jtabela.getModel();
        //Limpa a tabela antes de adicionar os novos dados
        int tam = model.getRowCount();
        for(int i=0;i<tam;i++){
            model.removeRow(0);
        }

    }
     
     private void buscaReal(){
         this.limparTabela();
         BuscaReal processo = new BuscaReal(jmensagem,jtabela, this.lista);
         processo.start();
         jbusca.setText("");
        jbusca.requestFocus();
         
     }//Fim da Busca de Amostras
     
     private void buscaRealCodigo(){
        this.limparTabela();
        this.lista = Banco.getBanco().consultaReal(jbusca.getText());
        //java.text.SimpleDateFormat formatodata = new java.text.SimpleDateFormat("dd/MM/yyyy"); 
        DefaultTableModel modelo = (DefaultTableModel) jtabela.getModel();

        for (Real real : this.lista) {
                   String[] linha = new String[6];
                   linha[0] = real.getCaixa()+"";
                   linha[1] = real.getCodigoR()+"";
                   linha[2] = real.getNome();
                   linha[3] = real.getDataCongelamento();
                   linha[4] = real.getRepetida()+"";
                   linha[5] = real.getLocal();
                   modelo.addRow(linha);
          }
        jbusca.setText("");
        jbusca.requestFocus();
     }//Fimda Busca Amostra
    
   
