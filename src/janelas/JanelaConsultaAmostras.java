    private void jregistroKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jregistroKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            this.buscarAmostrasRegistro();
        }else{
            if(evt.getKeyCode() == KeyEvent.VK_DOWN){
                jtabela.requestFocus();
            }else{
                if(evt.getKeyCode() == KeyEvent.VK_F5){
                    this.buscarAmostras();
                }else{
                    if(evt.getKeyCode() == KeyEvent.VK_F3){
                        this.buscarAmostrasPaciente();
                    }
                }
            }
        }
    }//GEN-LAST:event_jregistroKeyPressed

