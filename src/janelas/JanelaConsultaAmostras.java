     private void remover(){
         int sim = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja remover a amostra?", "Exclusão de Amostra", JOptionPane.YES_NO_OPTION);
         if(sim == JOptionPane.YES_OPTION){
             int item = jtabela.getSelectedRow();
             Banco.getBanco().excluirAmostra(this.lista.get(item).getId());
             JOptionPane.showMessageDialog(null, "Amostra excluída com sucesso!", "Confirmação de Exclusão", JOptionPane.INFORMATION_MESSAGE);
             this.buscarAmostras();
         }
     }
     
     private void buscarAmostrasPaciente(){
         Paciente paciente = new Paciente();
         JanelaConsultaPacientes janela = new JanelaConsultaPacientes(parent, true, paciente);
         janela.setVisible(true);
         
         if(paciente.getId() != 0){
            jpaciente.setText(paciente.getNome());

            this.limparTabela();

            this.lista = Banco.getBanco().consultaAmostras(paciente.getId());
            java.text.SimpleDateFormat formatodata = new java.text.SimpleDateFormat("dd/MM/yyyy"); 
            DefaultTableModel modelo = (DefaultTableModel) jtabela.getModel();

            for (Amostra amostra : this.lista) {
               String[] linha = new String[5];
               linha[0] = amostra.getRegistro()+"";

               linha[1] = formatodata.format(amostra.getData());
               linha[2] = amostra.getPaciente().getNome();
               linha[3] = amostra.getBloco()+"/"+amostra.getEnfermaria()+"/"+amostra.getLeito();
               linha[4] = amostra.getMedico();
               modelo.addRow(linha);
              }
         }
         jregistro.requestFocus();
         
     }
     
